#include "test.h"

using ss_ty = std::stringstream;

//==============================================================================
void test_exchange() {
  int a = 42;
  int b = 100;
  int c = std::exchange(a, b);
  ss_ty ss;
  ss << "a: " << a << ", b: " << b << ", c: " << c << "\n";
  out(ss.str());
}

//==============================================================================
void test() { TEST(test_exchange); }

//==============================================================================
int main() { test(); }
